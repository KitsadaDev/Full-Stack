let arr = [1,3,5,7,9,11]; //ประกาศค่าArray
console.log(arr);
arr.push(13); //เพิ่มอิลิเมนต์เข้าArray
console.log(arr);
console.log(arr.length); //หาว่ามีกี่ตัว
console.log(arr.indexOf(7)); //หาตำแหล่งเลข7
console.log(arr.reverse()); //กลับด้านArray
arr.pop(); //เอาตัวขวาสุดออกไป
console.log(arr);
console.log(arr.concat(15)); //เพิ่มอิลิเมนต์เข้าArray
