const d = new Date(); //ประกาศตัวแปร
console.log(d.getFullYear()); //แสดงปี
console.log(d.getMonth()); //แสดงเดือน
console.log(d.getDate()); //แสดงวันที่
console.log(d.getHours()); //แสดงชั่วโมง
console.log(d.getMinutes()); //แสดงนาที
console.log(d.getSeconds()); //แสดงวินาที
console.log(d.getMilliseconds()); //แสดงเสี้ยววินาที
